// - Скласти розклад на тиждень за домопоги switch. Користувач вводить порядковий номер дня тижня і на екрані відображається інфа що заплановано на цей день (можна замість плану на день, назву дня англійською).

let day = 1;

switch (day) {
    case 1:
        console.log('MON');
        break;
    case 2:
        console.log('TUE');
        break;
    case 3:
        console.log('WED');
        break;
    case 4:
        console.log('THU');
        break;
    case 5:
        console.log('FRI');
        break;
    case 6:
        console.log('SUT');
        break;
    case 7:
        console.log('SUN');
        break;
    default:
        console.log('error');
        break;
}